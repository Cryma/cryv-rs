    pad::disable_control_action(0, 27, true); // INPUT_PHONE
    pad::disable_control_action(0, 28, true); // INPUT_SPECIAL_ABILITY
    pad::disable_control_action(0, 29, true); // INPUT_SPECIAL_ABILITY_SECONDARY
